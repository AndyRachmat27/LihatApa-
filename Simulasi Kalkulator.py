print ("Simulasi Kalkulator")
#input data
operand1 = 28
operator = 4
operand2 = 10
#Rumus
#Penjumlahan
hasil = operand1 + operator + operand2
print (operand1,"+",operator,"+",operand2,"=",hasil)

#Pengurangan
hasil = operand1 - operator - operand2
print (operand1,"-",operator,"-",operand2,"=",hasil)

#Perkalian
hasil = operand1 * operator * operand2
print (operand1,"*",operator,"*",operand2,"=",hasil)

#Pembagian
hasil = operand1 / operator / operand2
print (operand1,"/",operator,"/",operand2,"=",hasil)